void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance==USART2)
	{
		decode_ic_data();
		HAL_UART_Receive_IT(&huart2,&ic_data,1);//恢复接受中断
	}
	if(huart->Instance==USART3)
	{
		read_QRCode_data();
		HAL_UART_Receive_IT(&huart3,QRCode_data,sizeof(QRCode_data));//恢复接受中断
	}
	if(huart->Instance==UART5)
	{
		decode_WHT101_data();
		HAL_UART_Receive_IT(&huart5,&yaw_data,1);//恢复接受中断
	}
		if(huart->Instance==UART7)
	{
			decode_line_data();
		HAL_UART_Receive_IT(&huart7,&line_data,1);//恢复接受中断
	}
	if(huart->Instance==USART6)
	{
		decode_openmv_data();
		HAL_UART_Receive_IT(&huart6,&openmv_data,1);//恢复接受中断
	}
	if(huart->Instance==USART1)
	{
		read_RemoteCrol_data();
		HAL_UART_Receive_IT(&huart1,remote_control_buffer,18);//恢复接受中断
	}
}

